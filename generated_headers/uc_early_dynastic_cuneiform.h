/*
 * Unicode Block: Early Dynastic Cuneiform
 * Range: U+12480..U+1254F
 * Properties: Unicode 17.0.0
 * Block Data: Unicode 17.0.0
 * -------------------------------------------------------------
 * Early Dynastic Cuneiform is a Unicode block of the Supplementary
 * Multilingual Plane (SMP), at U+12480–U+1254F, introduced in version
 * 8.0 (June 2015).
 * It is a supplement to the earlier encoding of the cuneiform script in
 * the two blocks U+12000–U+123FF " Cuneiform " and U+12400–U+1247F "
 * Cuneiform Numbers and Punctuation ".
 * "Early Dynastic Cuneiform" is designed to provide cuneiform signs used
 * during one of the earliest phases of cuneiform writing,
 * the Early Dynastic Period (c. 2900–2350 BC), also known as archaic
 * cuneiform , but discontinued in the Ur III period .
 * The original Cuneiform block, introduced in version 5.0 (July 2006) is
 * designed for the requirements of Ur III era cuneiform, with the
 * younger ( Old Assyrian and Neo-Assyrian ) literary tradition to be
 * considered font variants (analogous to the precedent of the approach
 * followed in Han unification ).
 * Even for the Ur III era, many signs recognized in relevant
 * dictionaries did not receive their own code point but are intended as
 * being expressed as ligatures of two or more constituent signs , to be
 * handled by the font , but for the purposes of representing archaic
 * cuneiform, the inventory of the original block was recognized as
 * insufficient and an additional 196 characters were added in version
 * 8.0. The sign inventory is mostly based on the 1922 dictionary Liste
 * der archaischen Keilschriftzeichen (LAK), with a substantial number of
 * characters (U+124D5 to U+12518) identified by their LAK number (or as
 * composed of characters identified by their LAK number) rather than
 * attempting to identify them by a reconstructed phonetic value.
 * The LAK has 870 signs in total, most of which are already covered in
 * the previous Unicode blocks in the form of their Ur III continuants.
 * The Preliminary Proposal for the block was submitted in 2012.
 *
 */
