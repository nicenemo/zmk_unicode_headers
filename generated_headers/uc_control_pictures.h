/*
 * Unicode Block: Control Pictures
 * Range: U+2400..U+243F
 * Properties: Unicode 17.0.0
 * Block Data: Unicode 17.0.0
 * -------------------------------------------------------------
 * Control Pictures is a Unicode block containing characters for
 * graphically representing the C0 control codes , and other control
 * characters. Its block name in Unicode 1.0 was Pictures for Control
 * Codes .
 * Code points U+0000 NULL to U+0020 SPACE can be mapped onto their
 * corresponding control picture by incrementing the code point by
 * 0x2400. For example, the corresponding control picture for U+000A LINE
 * FEED is 0x000A + 0x2400 = 0x240A (␊).
 *
 */
