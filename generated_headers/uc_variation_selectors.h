/*
 * Unicode Block: Variation Selectors
 * Range: U+FE00..U+FE0F
 * Properties: Unicode 17.0.0
 * Block Data: Unicode 17.0.0
 * -------------------------------------------------------------
 * Variation Selectors is a Unicode block containing 16 variation
 * selectors used to specify a glyph variant for a preceding character.
 * They are currently used to specify standardized variation sequences
 * for mathematical symbols, emoji symbols, 'Phags-pa letters, and CJK
 * unified ideographs corresponding to CJK compatibility ideographs . At
 * present only standardized variation sequences with VS1–VS4, VS7, VS15
 * and VS16 have been defined; VS15 and VS16 are reserved to request that
 * a character should be displayed as text or as an emoji respectively.
 * These combining characters are named variation selector-1 (for U+FE00)
 * through to variation selector-16 (U+FE0F), and are abbreviated VS1 –
 * VS16. Each applies to the immediately preceding character.
 *
 */
