/*
 * Unicode Block: Ideographic Description Characters
 * Range: U+2FF0..U+2FFF
 * Properties: Unicode 17.0.0
 * Block Data: Unicode 17.0.0
 * -------------------------------------------------------------
 * Ideographic Description Characters is a Unicode block containing
 * graphic characters used for describing CJK ideographs . They are used
 * in Ideographic Description Sequences (IDS) to provide a description of
 * an ideograph, in terms of what other ideographs make it up and how
 * they are laid out relative to one another. An IDS provides the reader
 * with a description of an ideograph that cannot be represented
 * properly, usually because it is not encoded in Unicode; rendering
 * systems are not intended to automatically compose the pieces into a
 * complete ideograph, and the descriptions are not standardized.
 * U+2FF0 to U+2FFB were introduced from GBK ; U+2FFC to U+2FFF were
 * devised later and introduced in Unicode 15.1 (2023).
 *
 */
#ifndef UC_H_IDEOGRAPHIC_DESCRIPTION_CHARACTERS
#define UC_H_IDEOGRAPHIC_DESCRIPTION_CHARACTERS

#define UC_MACRO_NAME_2FF0 0x2FF0 // ⿰ IDEOGRAPHIC DESCRIPTION CHARACTER LEFT TO RIGHT
#define UC_MACRO_NAME_2FF1 0x2FF1 // ⿱ IDEOGRAPHIC DESCRIPTION CHARACTER ABOVE TO BELOW
#define UC_MACRO_NAME_2FF2 0x2FF2 // ⿲ IDEOGRAPHIC DESCRIPTION CHARACTER LEFT TO MIDDLE AND RIGHT
#define UC_MACRO_NAME_2FF3 0x2FF3 // ⿳ IDEOGRAPHIC DESCRIPTION CHARACTER ABOVE TO MIDDLE AND BELOW
#define UC_MACRO_NAME_2FF4 0x2FF4 // ⿴ IDEOGRAPHIC DESCRIPTION CHARACTER FULL SURROUND
#define UC_MACRO_NAME_2FF5 0x2FF5 // ⿵ IDEOGRAPHIC DESCRIPTION CHARACTER SURROUND FROM ABOVE
#define UC_MACRO_NAME_2FF6 0x2FF6 // ⿶ IDEOGRAPHIC DESCRIPTION CHARACTER SURROUND FROM BELOW
#define UC_MACRO_NAME_2FF7 0x2FF7 // ⿷ IDEOGRAPHIC DESCRIPTION CHARACTER SURROUND FROM LEFT
#define UC_MACRO_NAME_2FF8 0x2FF8 // ⿸ IDEOGRAPHIC DESCRIPTION CHARACTER SURROUND FROM UPPER LEFT
#define UC_MACRO_NAME_2FF9 0x2FF9 // ⿹ IDEOGRAPHIC DESCRIPTION CHARACTER SURROUND FROM UPPER RIGHT
#define UC_MACRO_NAME_2FFA 0x2FFA // ⿺ IDEOGRAPHIC DESCRIPTION CHARACTER SURROUND FROM LOWER LEFT
#define UC_MACRO_NAME_2FFB 0x2FFB // ⿻ IDEOGRAPHIC DESCRIPTION CHARACTER OVERLAID
#define UC_MACRO_NAME_2FFC 0x2FFC // ⿼ IDEOGRAPHIC DESCRIPTION CHARACTER SURROUND FROM RIGHT
#define UC_MACRO_NAME_2FFD 0x2FFD // ⿽ IDEOGRAPHIC DESCRIPTION CHARACTER SURROUND FROM LOWER RIGHT
#define UC_MACRO_NAME_2FFE 0x2FFE // ⿾ IDEOGRAPHIC DESCRIPTION CHARACTER HORIZONTAL REFLECTION
#define UC_MACRO_NAME_2FFF 0x2FFF // ⿿ IDEOGRAPHIC DESCRIPTION CHARACTER ROTATION


#endif // UC_H_IDEOGRAPHIC_DESCRIPTION_CHARACTERS
