/*
 * Unicode Block: Modifier Tone Letters
 * Range: U+A700..U+A71F
 * Properties: Unicode 17.0.0
 * Block Data: Unicode 17.0.0
 * -------------------------------------------------------------
 * Modifier Tone Letters is a Unicode block containing tone markings for
 * Chinese, Chinantec , Africanist, and other phonetic transcriptions. It
 * does not contain the standard IPA tone marks, which are found in
 * Spacing Modifier Letters .
 * ⟨ ꜀◌ ꜁◌ ꜂◌ ꜃◌ ◌꜄ ◌꜅ ◌꜆ ◌꜇ ⟩ are used to mark yin and (underlined) yang
 * splits of the ping, shang, qu and ru tones, respectively, in the
 * etymological four-tone analysis of Chinese. The dotted tone letters ⟨
 * ꜈ ꜉ ꜊ ꜋ ꜌ ⟩ are used for the pitch of neutral tones , while the
 * reversed tone letters ⟨ ꜒ ꜓ ꜔ ꜕ ꜖ ⟩ and neutral ⟨ ꜍ ꜎ ꜏ ꜐ ꜑ ⟩ are used
 * for tone sandhi . ⟨ ꜗ ꜘ ꜙ ꜚ ⟩ are modifier letters used in Ozumacín
 * Chinantec . ⟨ ꜛ ꜜ ⟩ are the IPA modifier letters for upstep and
 * downstep , while ⟨ ꜝ ꜞ ꜟ ⟩ are substitutes people used before broad
 * font support of the IPA, and still preferred by some.
 *
 */
